package com.hixx.web.data.entity;

public class CityScore {
	private int code;
	private String writer;
	private int countryCode;
	private int trate;
	private int frate;
	private int srate;
	
	public int getCode() {
		return code;
	}
	public void setCode(int code) {
		this.code = code;
	}
	public String getWriter() {
		return writer;
	}
	public void setWriter(String writer) {
		this.writer = writer;
	}
	public int getCountryCode() {
		return countryCode;
	}
	public void setCountryCode(int countryCode) {
		this.countryCode = countryCode;
	}
	public int getTrate() {
		return trate;
	}
	public void setTrate(int trate) {
		this.trate = trate;
	}
	public int getFrate() {
		return frate;
	}
	public void setFrate(int frate) {
		this.frate = frate;
	}
	public int getSrate() {
		return srate;
	}
	public void setSrate(int srate) {
		this.srate = srate;
	}
}
